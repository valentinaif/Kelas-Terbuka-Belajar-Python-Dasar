# ELIF : ELSE IF STATMENT

nama = input("Nama kamu siapa? ")

if nama == "firdaus" : # kondisi 1
	print("Hai ganteng!") # aksi true 1
elif nama == "imaduddin" : # kondisi 2
	print("Hai si kece abis!") # aksi true 2
elif nama == "ibrahim" : # kondisi 3
	print("Hai humoris!") # aksi true 3
else : 
	print("Sorry nggak kenal") # aksi false
print("Akhir dari program")
	